#pragma once

/**
 * @brief Add two numbers
 * 
 * @param firts_number First number
 * @param second_number Second number
 * @return firts_number + second_number
*/
int add(int firts_number, int second_number);

/**
 * @brief Subtract two numbers
 * 
 * @param firts_number First number
 * @param second_number Second number
 * @return firts_number - second_number
*/
int subtract(int firts_number, int second_number);

/**
 * @brief Multiply two numbers
 * 
 * @param firts_number First number
 * @param second_number Second number
 * @return firts_number * second_number
*/
int multiply(int firts_number, int second_number);

/**
 * @brief Divide two numbers
 * 
 * @param firts_number First number
 * @param second_number Second number
 * @return firts_number / second_number iff second_number != 0
*/
double divide(int firts_number, int second_number);
